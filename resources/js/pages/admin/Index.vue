<script setup lang="ts">
import MagazinesTable from '@/components/admin/tables/MagazinesTable.vue';
import MangasTable from '@/components/admin/tables/MangasTable.vue';
import PeopleTable from '@/components/admin/tables/PeopleTable.vue';
import TagsTable from '@/components/admin/tables/TagsTable.vue';
import UsersTable from '@/components/admin/tables/UsersTable.vue';
import { SharedData } from '@/types';
import { adminTabItems } from '@/utils/constants';
import { Head, router, usePage } from '@inertiajs/vue3';
import { breakpointsTailwind, useBreakpoints } from '@vueuse/core';
import { ref } from 'vue';

const page: { props: SharedData } = usePage();

const breadcrumbItems = [
    {
        label: 'Admin',
        class: 'text-xl sm:text-2xl lg:text-3xl',
        icon: 'lucide:monitor-cog',
    },
];

const activeTab = ref(page.props.ziggy.location.split('/')[1]);

const handleTabChange = (tab: string | number) => {
    //No cambiar si es la vista actual
    if (tab === activeTab.value) return;

    activeTab.value = tab as string;
    router.visit(route('admin.index', tab), { preserveScroll: true, replace: true });
};

const activeBreakpoint = useBreakpoints(breakpointsTailwind).active();
</script>

<template>
    <Head title="Panel de administraciÃ³n" />
    <UBreadcrumb :items="breadcrumbItems" :ui="{ linkLeadingIcon: 'size-8' }" class="select-none" />
    <UTabs
        :items="adminTabItems"
        class="mt-8"
        variant="link"
        :model-value="activeTab"
        @update:model-value="handleTabChange"
        :ui="{ label: activeBreakpoint || 'hidden' }"
    >
        <template #mangas><MangasTable /></template>
        <template #people><PeopleTable /></template>
        <template #magazines><MagazinesTable /></template>
        <template #users><UsersTable /></template>
        <template #tags><TagsTable /></template>
    </UTabs>
</template>
